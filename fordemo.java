package day2;

public class ForDemo {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++) {
			System.out.print(i+" ");
		}
		System.out.println(" ");
		for(int i=10;i>0;i--) {
			System.out.print(i+" ");
		}
		System.out.println(" ");
		
		for(int i=1;i<=10;i++) {
			System.out.println("2 x " + i + " = " + 2*i);
		}
		System.out.println("out of the for loop");

	}

}
